		/// <inheritdoc />
		public override void MapFromModel(Models.Year m)
		{
			if (m == null)
			{
				CSGenio.framework.Log.Error("Map Model (Year) to ViewModel (Year) - Model is a null reference");
				throw new ModelNotFoundException("Model not found");
			}

			try
			{
				ValYear_number = ViewModelConversion.ToNumeric(m.ValYear_number);
				ValCodyear = ViewModelConversion.ToString(m.ValCodyear);
			}
			catch (Exception)
			{
				CSGenio.framework.Log.Error("Map Model (Year) to ViewModel (Year) - Error during mapping");
				throw;
			}
		}

